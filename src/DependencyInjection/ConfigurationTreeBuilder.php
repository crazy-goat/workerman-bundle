<?php

declare(strict_types=1);

namespace CrazyGoat\WorkermanBundle\DependencyInjection;

use Symfony\Component\Config\Definition\Configurator\DefinitionConfigurator;

final readonly class ConfigurationTreeBuilder
{
    public function configure(DefinitionConfigurator $definition): void
    {
        $root = $definition->rootNode();

        $root->addDefaultsIfNotSet()
            ->children()
                ->scalarNode('runtime_dir')
                    ->info('Writable runtime directory for cache, logs, and PID files. In PHAR mode defaults to directory containing the PHAR. Can be overridden with WORKERMAN_RUNTIME_DIR env var.')
                    ->defaultValue('%kernel.project_dir%')
                    ->end()
                ->scalarNode('user')
                    ->info('Unix user of processes. Default: current user')
                    ->defaultNull()
                    ->end()
                ->scalarNode('group')
                    ->info('Unix group of processes. Default: current group')
                    ->defaultNull()
                    ->end()
                ->integerNode('stop_timeout')
                    ->info('Max seconds of child process work before force kill')
                    ->defaultValue(2)
                    ->end()
                ->integerNode('cache_warmup_timeout')
                    ->info('Max seconds to wait for cache warmup in forked process. Can be overridden with WORKERMAN_CACHE_WARMUP_TIMEOUT env var.')
                    ->min(1)
                    ->defaultValue(30)
                    ->end()
                ->integerNode('status_timeout')
                    ->info('Max seconds to wait for status file generation after sending SIGIOT')
                    ->defaultValue(5)
                    ->end()
                ->scalarNode('pid_file')
                    ->info('File to store master process PID')
                    ->cannotBeEmpty()
                    ->defaultValue('%kernel.project_dir%/var/run/workerman.pid')
                    ->end()
                ->scalarNode('log_file')
                    ->info('Log file')
                    ->cannotBeEmpty()
                    ->defaultValue('%kernel.project_dir%/var/log/workerman.log')
                    ->end()
                ->scalarNode('stdout_file')
                    ->info('File to write all output (echo var_dump, etc.) to when server is running as daemon')
                    ->cannotBeEmpty()
                    ->defaultValue('%kernel.project_dir%/var/log/workerman.stdout.log')
                    ->end()
                ->integerNode('max_package_size')
                    ->info('Max package size can be received')
                    ->defaultValue(10 * 1024 * 1024)
                    ->end()
                ->integerNode('response_chunk_size')
                    ->info('Response chunk size')
                    ->defaultValue(2048)
                    ->end()
                ->arrayNode('servers')
                    ->prototype('array')
                        ->addDefaultsIfNotSet()
                        ->children()
                            ->scalarNode('name')
                                ->info('Server process name')
                                ->isRequired()
                                ->cannotBeEmpty()
                                ->end()
                            ->scalarNode('listen')
                                ->info('Listen address. Supported protocols: http://, https://, ws://, wss://, tcp://')
                                ->defaultNull()
                                ->example('http://0.0.0.0:80')
                                ->end()
                            ->scalarNode('local_cert')
                                ->info('Path to local certificate file on filesystem')
                                ->defaultNull()
                                ->end()
                            ->scalarNode('local_pk')
                                ->info('Path to local private key file on filesystem')
                                ->defaultNull()
                                ->end()
                            ->integerNode('processes')
                                ->info('Number of webserver worker processes. Default: number of CPU cores * 2')
                                ->defaultNull()
                                ->end()
                            ->booleanNode('reuse_port')
                                ->info('Set whether the current worker enables the reuse of listening ports (SO_REUSEPORT).')
                                ->defaultFalse()
                                ->end()
                            ->booleanNode('serve_files')
                                ->info('Should current worker serve files from public directory')
                                ->defaultFalse()
                                ->setDeprecated('workerman', '0.9.3', 'Use static file middleware instead.')
                                ->end()
                            ->scalarNode('root_dir')
                                ->info('Should current worker serve files from public directory')
                                ->setDeprecated('workerman', '0.9.3', 'Use static file middleware instead.')
                                ->defaultNull()
                                ->example('%kernel.project_dir%/public')
                                ->end()
                            ->arrayNode('middlewares')
                                ->info('A list of service IDs to be used as middleware.')
                                    ->prototype('scalar')->end()
                                ->end()
                            ->end()
                        ->end()
                    ->end()
                ->arrayNode('reload_strategy')
                    ->info('Reload strategies configuration')
                    ->addDefaultsIfNotSet()
                    ->children()
                        ->arrayNode('exception')
                            ->info('Reload worker each time that an exception is thrown during the request handling')
                            ->addDefaultsIfNotSet()
                            ->children()
                                ->booleanNode('active')
                                    ->info('Is exception strategy active')
                                    ->defaultTrue()
                                    ->end()
                                ->arrayNode('allowed_exceptions')
                                    ->info('List of allowed exceptions that do not trigger a reload')
                                    ->prototype('scalar')->end()
                                    ->defaultValue([
                                        \Symfony\Component\HttpKernel\Exception\HttpExceptionInterface::class,
                                        'Symfony\Component\Serializer\Exception\ExceptionInterface',
                                    ])
                                    ->end()
                                ->end()
                            ->end()
                        ->arrayNode('max_requests')
                            ->info('Reload worker on every N request to prevent memory leaks')
                            ->addDefaultsIfNotSet()
                            ->children()
                                ->booleanNode('active')
                                    ->info('Is max_requests strategy active')
                                    ->defaultFalse()
                                    ->end()
                                ->integerNode('requests')
                                    ->info('Maximum number of requests after which the worker will be reloaded')
                                    ->defaultValue(1000)
                                    ->end()
                                ->integerNode('dispersion')
                                    ->info('Prevent simultaneous restart of all workers (1000 requests and 20% dispersion will restart between 800 and 1000)')
                                    ->defaultValue(20)
                                    ->end()
                                ->end()
                            ->end()
                        ->arrayNode('file_monitor')
                            ->info('Reload all workers each time you change the code')
                            ->addDefaultsIfNotSet()
                            ->children()
                                ->booleanNode('active')
                                    ->info('Is file_monitor strategy active')
                                    ->defaultFalse()
                                    ->end()
                                ->arrayNode('source_dir')
                                    ->info('Source directories for file monitoring')
                                    ->prototype('scalar')->end()
                                    ->defaultValue([
                                        '%kernel.project_dir%/src',
                                        '%kernel.project_dir%/config',
                                    ])
                                    ->end()
                                ->arrayNode('file_pattern')
                                    ->info('Monitored file patterns')
                                    ->prototype('scalar')->end()
                                    ->defaultValue([
                                        '*.php',
                                        '*.yaml',
                                    ])
                                    ->end()
                                ->end()
                            ->end()
                        ->arrayNode('always')
                            ->info('Reload worker after each request')
                            ->addDefaultsIfNotSet()
                            ->children()
                                ->booleanNode('active')
                                    ->info('Is always strategy active')
                                    ->defaultFalse()
                                    ->end()
                                ->end()
                            ->end()
                        ->arrayNode('memory')
                            ->info('Reload worker when memory usage reaches a certain threshold')
                            ->addDefaultsIfNotSet()
                            ->children()
                                ->booleanNode('active')
                                    ->info('Is memory strategy active')
                                    ->defaultFalse()
                                ->end()
                                ->integerNode('limit')
                                    ->info('Maximum memory usage after which the worker will be reloaded')
                                    ->defaultValue(134_217_728)
                                ->end()
                                ->integerNode('gc_limit')
                                    ->info('Maximum memory usage after which gc_collect_cycles will be called to free memory')
                                    ->defaultValue(100_663_296)
                                ->end()
                            ->end()
                        ->end()
                    ->end()
                ->end()
                ->arrayNode('build')
                    ->info('PHAR and standalone binary build configuration')
                    ->addDefaultsIfNotSet()
                    ->children()
                        ->scalarNode('build_dir')
                            ->info('Output directory for built artifacts')
                            ->defaultValue('%kernel.project_dir%/build')
                            ->end()
                        ->scalarNode('kernel_class')
                            ->info('Fully qualified class name of the Symfony Kernel (e.g. App\\Kernel)')
                            ->defaultValue('App\\Kernel')
                            ->end()
                        ->scalarNode('phar_filename')
                            ->info('Name of the generated PHAR file')
                            ->defaultValue('app.phar')
                            ->end()
                        ->scalarNode('bin_filename')
                            ->info('Name of the generated standalone binary')
                            ->defaultValue('app.bin')
                            ->end()
                        ->scalarNode('bin_php_version')
                            ->info('PHP version for the static PHP binary (phpmicro.sfx). Default: current PHP version')
                            ->defaultNull()
                            ->end()
                        ->arrayNode('sfx')
                            ->info('Configuration for the phpmicro SFX binary used in BIN mode')
                            ->addDefaultsIfNotSet()
                            ->children()
                                ->scalarNode('url')
                                    ->info('Custom URL to download phpmicro.sfx from')
                                    ->defaultNull()
                                    ->end()
                                ->scalarNode('file')
                                    ->info('Local path to an existing phpmicro.sfx binary')
                                    ->defaultNull()
                                    ->end()
                                ->scalarNode('sha256')
                                    ->info('SHA-256 hex digest of the SFX binary. When set, the downloaded SFX is verified against it. Strongly recommended.')
                                    ->defaultNull()
                                    ->end()
                                ->booleanNode('allow_insecure')
                                    ->info('Disable TLS peer verification when downloading the SFX. Off by default. Use only for local mirrors with self-signed certificates.')
                                    ->defaultFalse()
                                    ->end()
                                ->end()
                            ->end()
                        ->arrayNode('exclude_patterns')
                            ->info('Additional regex patterns for files to exclude from the PHAR (added on top of built-in defaults). Note: .env* files at project root and tests/, var/, build/, .git/, .github/, docs/ are always excluded.')
                            ->prototype('scalar')->end()
                            ->defaultValue([])
                            ->end()
                        ->arrayNode('exclude_files')
                            ->info('Specific files to exclude from the PHAR (relative to project root)')
                            ->prototype('scalar')->end()
                            ->defaultValue([])
                            ->end()
                        ->scalarNode('custom_ini')
                            ->info('Custom php.ini directives for the standalone binary (BIN mode only)')
                            ->defaultNull()
                            ->end()
                        ->end()
                    ->end()
                ->end()
                ->end();
    }
}
